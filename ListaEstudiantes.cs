public class Estudiante{
    public string Nombre;
    public int Edad;
    public string Carnet;
    public bool Solvencia;

    public Estudiante(string Nombre, int Edad, string Carnet, bool Solvencia){
        this.Nombre = Nombre;

        this.Edad = Edad;

        this.Carnet = Carnet;

        this.Solvencia = Solvencia;
    }
}